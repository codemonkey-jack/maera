<div class="maera-general-content">
	<h1 class="maera-general-title">Welcome to the Maera Theme</h1>
	<hr>
	<p class="big">Maera is one of the most versatile and modular WordPress Theme Frameworks, allowing both developers and novice users to build amazing sites and take advantage of its unique features.</p>
	<div style="height: 1em;"></div>
	<p>Maera introduces the concept of <strong>shells</strong>, essentially child themes built as plugins that can change and affect every aspect of your theme and presentation, allowing you to build a completely customized experience for your site and your users.</p>

	<hr>

	<div class="columns3 first">
		<span class="title-icon dashicons dashicons-archive"></span>
		<h2>Modular in nature</h2>
		<p>The core of the theme is a set of libraries that allows you to extend it and use it however you like! Using <strong>Shells</strong> and plugins you can add or remove features and customize your site using just the bits and pieces that you need.</p>
	</div>

	<div class="columns3 second">
		<span class="title-icon dashicons dashicons-admin-plugins"></span>
		<h2>Plugins</h2>
		<p>Instead of bundling extra libraries in the plugin, we are requiring you to install some 3rd-party plugins. This way you can get updates for them without the need to update the core of your theme, while at the same time makes it easier for developers to tackle issues when the need arises.</p>
	</div>

	<div class="columns3 third">
		<span class="title-icon dashicons dashicons-editor-code"></span>
		<h2>Documentation</h2>
		<p>We have extended documentation both in the [repository wiki on github](https://github.com/presscodes/maera/wiki) as well as embeded in the theme options page. Just click on the <strong>Documentation</strong> tab on the top and you'll be able to learn more.
	</div>

	<div style="clear:both;float:none;"></div>
	<hr>

	<div class="columns3 first">
		<span class="title-icon dashicons dashicons-carrot"></span>
		<h2>Uses <a href="http://twig.sensiolabs.org/doc/templates.html">Twig</a></h2>
		<p>Twig is an amazing templating engine that uses a simple syntax, allowing non-php developers to understand and tweak their own templates, further customizing their own site. You can find these templates in the <code>/views</code> folder in your theme.</p>
	</div>

	<div class="columns3 second">
		<span class="title-icon dashicons dashicons-dashboard"></span>
		<h2>Fast and SEO Optimized</h2>
		<p>Taking advantage of internal caching and SEO Optimizations, you can be sure your site will be fast and performant for users as well as search engines.</p>
	</div>

	<div class="columns3 third">
		<span class="title-icon dashicons dashicons-universal-access-alt"></span>
		<h2>Accessible</h2>
		<p>We designed this theme with accessibility standards in mind so everyone will be able to use your sites and try to educate developers so that all future shells will also be accessible.
